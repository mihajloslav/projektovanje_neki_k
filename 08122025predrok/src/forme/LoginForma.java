    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        
        String email = jTextFieldEmail.getText();
        String sifra = String.valueOf(jPasswordFieldSifra.getPassword());
        if(email == null || email.trim().isEmpty() || !email.contains("@") || !email.contains(".")){
            JOptionPane.showMessageDialog(this, "Niste uneli ispravan email");
            return;
        }
        if(sifra == null || sifra.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Niste uneli ispravnu sifru");
            return;
        }
        
        
        
        boolean uspesno = Controller.getInstance().UlogujNastavnika(email,sifra);
        if(uspesno){
            GlavnaForma gF = new GlavnaForma();
            gF.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, "Niste uneli ispravne podatke");
            return;
        }
        
        
    }//GEN-LAST:event_jButtonLoginActionPerformed

