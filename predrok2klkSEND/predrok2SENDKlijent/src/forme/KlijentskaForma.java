    public void proveri(int sekunde){
        if(ulogovani == null){
            if(sekunde >= 10)
                this.dispose();
            setTitle("Forma će se ugasiti za: " + (10 - sekunde) + " sekundi ako se ne prijavite!");
        }
        else
            setTitle("Ulogovani ste kao: " + ulogovani.getUsername());
        
    
    }
