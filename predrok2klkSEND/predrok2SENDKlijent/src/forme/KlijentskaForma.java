    private void jButtonPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPosaljiActionPerformed
        String tekst = txtKomanda.getText().trim();
        String[] split = tekst.split(" ");
                if(tekst.isEmpty() || tekst.isBlank()){
            txtKomanda.setText("");
            jTextAreaChat.append(" [ERROR]: NISTE LEPO UNELI KOMANDU!\n");
            return;
        }
        System.out.println(split[0].toUpperCase());
        switch (split[0].toUpperCase()) {
            case "LOGIN":
                if(ulogovani == null){
                    Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.LOGIN, tekst));
                ServerskiOdgovor so =(ServerskiOdgovor) Komunikacija.getInstance().primiOdgovor();
                
                if(so.getOdgovor() != null){
                    ulogovani = (Korisnik)so.getOdgovor();
                    Komunikacija.getInstance().getPn().start();
                }else
                    jTextAreaChat.append(so.getPoruka() + "\n");
                }
                else jTextAreaChat.append("[ERROR]: VEĆ STE ULOGOVANI\n"); 
                    
                break;
            case "SEND":
                System.out.println(tekst);
                Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.SEND, tekst));
                break;
            case "BROADCAST":
                Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.BROADCAST, tekst));
                break;
            case "LOGOUT":
                Komunikacija.getInstance().getPn().ugasiNit();
                Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.LOGOUT, "")); 
                ulogovani = null;
                this.dispose();
            default:
                jTextAreaChat.append("\n[ERROR]: UNELI STE NEPOSTOJEĆU KOMANDU\n");
        }
        
        txtKomanda.setText("");
        
    }//GEN-LAST:event_jButtonPosaljiActionPerformed

