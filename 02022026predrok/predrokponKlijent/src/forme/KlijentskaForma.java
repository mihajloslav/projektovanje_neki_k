    private void jButtonPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPosaljiActionPerformed
        String tekst = txtKomanda.getText().trim();
        String[] split = tekst.split(" ");
        ServerskiOdgovor so;
        if(tekst.isEmpty() || tekst.isBlank()){
            txtKomanda.setText("");
            JOptionPane.showMessageDialog(null, "NISTE LEPO UNELI KOMANDU");
            return;
        }
        System.out.println(split[0]);
        switch (split[0].toUpperCase()) {
            case "LOGIN":
                if(ulogovani == null){
                    Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.LOGIN, tekst));
                    so = (ServerskiOdgovor) Komunikacija.getInstance().primiOdgovor();
                    ulogovani = (Korisnik)so.getOdgovor();
                    JOptionPane.showMessageDialog(null, (String)so.getPoruka());
                }
                else JOptionPane.showMessageDialog(null, "VEÄ† STE ULOGOVANI");
                break;
            case "ADD":
                if(ulogovani != null){
                    Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.ADD, tekst));
                    so = (ServerskiOdgovor) Komunikacija.getInstance().primiOdgovor();
                    JOptionPane.showMessageDialog(null, (String)so.getPoruka());
                }
                else JOptionPane.showMessageDialog(null, "NISTE ULOGOVANI");
                break;
            case "MINMAX":
                if(ulogovani != null){
                    Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.MINMAX, tekst));
                    so = (ServerskiOdgovor) Komunikacija.getInstance().primiOdgovor();
                    JOptionPane.showMessageDialog(null, (String)so.getPoruka());
                }else JOptionPane.showMessageDialog(null, "NISTE ULOGOVANI");
                break;
            case "DATE":
                if(ulogovani != null){
                    Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.DATE, tekst));
                    so = (ServerskiOdgovor) Komunikacija.getInstance().primiOdgovor();
                    JOptionPane.showMessageDialog(null, (String)so.getPoruka());
                }else JOptionPane.showMessageDialog(null, "NISTE ULOGOVANI");
                break;
            case "LOGOUT":
                if(ulogovani != null){
                    Komunikacija.getInstance().posaljiZahtev(new KlijentskiZahtev(Operacije.LOGOUT, tekst));
                    this.dispose();
                }else JOptionPane.showMessageDialog(null, "NISTE ULOGOVANI");
                break;
            default:
                if(ulogovani != null)
                    JOptionPane.showMessageDialog(null, "NISTE LEPO UNELI KOMANDU");
                else JOptionPane.showMessageDialog(null, "NISTE ULOGOVANI");
                
        }
        txtKomanda.setText("");
    }//GEN-LAST:event_jButtonPosaljiActionPerformed

